export function useTranslation() {
  const tenant = getTenantFromDomain();
  const lang = TENANT_LANG[tenant] || 'en';
  const dict = TRANSLATIONS[lang];
  const optDict = OPTION_LABELS[lang] || {};
  const isEngland = tenant === 'england';

  const t = (key: string, fallback?: string): string => {
    if (isEngland && ENGLAND_OVERRIDES[key]) {
      return ENGLAND_OVERRIDES[key];
    }
    return dict[key] ?? fallback ?? key;
  };

  const o = (value: string): string => {
    return optDict[value] ?? value;
  };

  return { t, o, lang, isSpanish: lang === 'es', isFrench: lang === 'fr', isPortuguese: lang === 'pt', tenant };
}
